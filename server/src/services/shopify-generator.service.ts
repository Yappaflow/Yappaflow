const BRAND_LAYER_MAX_TOKENS = 6_000;
const MAX_ATTEMPTS           = 2;

export async function generateShopifyBundle(
  projectId: string,
  agencyId: string,
): Promise<{ filesCreated: number }> {
  const project = await Project.findOne({ _id: projectId, agencyId });
  if (!project) throw new Error("Project not found");
  if (!project.identity) throw new Error("Project has no identity — run extraction first");

  const identity = project.identity as IProjectIdentity;
  const promptProducts = productsForPrompt(identity);

  const direction = pickDesignDirection({
    tone:         identity.tone,
    industry:     identity.industry,
    businessName: identity.businessName,
    city:         identity.city,
    hasProducts:  Boolean(identity.products?.length),
  });
  log(`   ↳ design direction: ${direction.key} — "${direction.label}"`);

  const chooser = project.heroChooser as IHeroChooser | undefined;
  const lockedHero: string | undefined =
    chooser?.pickedVariantId && chooser?.variants?.length
      ? chooser.variants.find((v: IHeroVariant) => v.id === chooser.pickedVariantId)?.html
      : undefined;
  if (lockedHero) {
    log(`   ↳ locked hero: ${chooser?.pickedVariantId} (${lockedHero.length} chars)`);
  }

  // Load the scaffold eagerly — if this fails, there's no point calling
  // the AI.
  let scaffold: ScaffoldFile[];
  try {
    scaffold = await loadScaffold();
  } catch (err) {
    logError(`Failed to load Shopify scaffold at ${SCAFFOLD_DIR}`, err);
    throw new Error(`Shopify scaffold missing or unreadable at ${SCAFFOLD_DIR}: ${(err as Error).message}`);
  }
  log(`   ↳ loaded Shopify scaffold: ${scaffold.length} files`);

  // Expected file count for UI progress: scaffold + README + optional csv.
  const baseExpected = scaffold.length + 1 + (identity.products?.length ? 1 : 0);

  await Project.findByIdAndUpdate(projectId, {
    buildJobStatus:  "running",
    buildPhase:      "analyzing",
    buildFilesDone:  0,
    buildFilesTotal: baseExpected,
    buildError:      null,
    buildStartedAt:  new Date(),
    buildAttempt:    0,
    buildAttemptMax: MAX_ATTEMPTS,
  });

  const session = await AISession.create({
    agencyId,
    projectId,
    phase:  "generating",
    status: "active",
  });
  const sessionId = session._id;

  const identitySummary = {
    brandName: identity.businessName,
    tagline:   identity.tagline,
    industry:  identity.industry,
    tone:      identity.tone,
  };

  const basePrompt = getGenerateShopifyBrandPrompt({
    identity:   identitySummary,
    products:   promptProducts,
    direction,
    lockedHero,
  });

  const userContent =
    "## Task\n\n" +
    "Emit the brand-layer JSON for this store. JSON only. Begin with `{`.\n";

  log(
    `🛍  Generating Shopify brand layer for project ${projectId} ` +
    `(${identity.businessName}${promptProducts.length ? `, ${promptProducts.length} products` : ""})`,
  );

  let brand: BrandJson | null = null;
  let lastRaw = "";
  let lastIssues: PatchShopifyBrandIssue[] = [];

  for (let attempt = 1; attempt <= MAX_ATTEMPTS; attempt++) {
    await Project.findByIdAndUpdate(projectId, {
      buildPhase:   attempt === 1 ? "generating" : "patching",
      buildAttempt: attempt,
    });

    const systemPrompt =
      attempt === 1
        ? basePrompt
        : getPatchShopifyBrandPrompt({
            identity:     identitySummary,
            direction,
            lockedHero,
            previousJson: lastRaw,
            issues:       lastIssues,
          });

    let text: string;
    try {
      const res = await analyzeOnce(systemPrompt, userContent, {
        phase:     "generating",
        maxTokens: BRAND_LAYER_MAX_TOKENS,
      });
      text = res.text;
      await trackUsage(sessionId.toString(), res.usage);
    } catch (err) {
      logError(`Shopify brand-layer AI call failed (attempt ${attempt})`, err);
      await Project.findByIdAndUpdate(projectId, {
        buildJobStatus: "failed",
        buildPhase:     "failed",
        buildError:     (err as Error).message,
      });
      await AISession.findByIdAndUpdate(sessionId, { phase: "failed", status: "failed", error: (err as Error).message });
      throw err;
    }

    lastRaw = text;
    const parseResult = tryParseBrandJson(text);
    if (!parseResult.ok) {
      lastIssues = [{
        path:    "(root)",
        issue:   `Previous output failed JSON.parse: ${parseResult.error}`,
        suggest: "Return ONLY a JSON object, no prose, no markdown fences",
      }];
      log(`⚠️  Shopify brand-layer attempt ${attempt}/${MAX_ATTEMPTS}: JSON parse failed — ${parseResult.error}`);
      continue;
    }

    const issues = validateBrandJson(parseResult.value);
    if (issues.length === 0) {
      brand = parseResult.value;
      log(`✅ Shopify brand-layer attempt ${attempt}/${MAX_ATTEMPTS}: validated`);
      break;
    }

    lastIssues = issues;
    log(
      `⚠️  Shopify brand-layer attempt ${attempt}/${MAX_ATTEMPTS}: ${issues.length} issue(s) — ` +
      issues.slice(0, 3).map((i) => i.path).join(", ") +
      (issues.length > 3 ? `, …+${issues.length - 3}` : ""),
    );
  }

  if (!brand) {
    const msg =
      `Shopify brand-layer JSON failed validation after ${MAX_ATTEMPTS} attempts.\n` +
      `Last issues: ${lastIssues.map((i) => `${i.path}: ${i.issue}`).join("; ")}`;
    await Project.findByIdAndUpdate(projectId, {
      buildJobStatus: "failed",
      buildPhase:     "failed",
      buildError:     msg,
    });
    await AISession.findByIdAndUpdate(sessionId, { phase: "failed", status: "failed", error: msg });
    throw new Error(msg);
  }

  // Merge brand layer into the scaffold, deterministically.
  await Project.findByIdAndUpdate(projectId, { buildPhase: "packaging" });
  const themed = applyBrandLayer(scaffold, brand, identity);

  // Wipe previous artifacts for this project.
  await GeneratedArtifact.deleteMany({ agencyId, projectId });

  const allFiles: ScaffoldFile[] = themed.map((f) => ({
    ...f,
    filePath: `theme/${f.filePath}`,
  }));

  // Top-level README (outside the theme/ subdir).
  allFiles.unshift({
    filePath: "README.txt",
    content:
      `Yappaflow → Shopify one-click bundle\n` +
      `====================================\n\n` +
      `1) Theme upload (one click, no re-zipping needed)\n` +
      `   • Shopify Admin → Online Store → Themes → "Upload zip file".\n` +
      `   • Select: shopify-theme.zip  (already packaged the way Shopify expects).\n` +
      `   • Click "Publish" once the upload finishes.\n\n` +
      (identity.products?.length
        ? `2) Product import\n` +
          `   • In Shopify Admin: Products → Import.\n` +
          `   • Select: products.csv\n` +
          `   • Confirm — Shopify will create products with variants and images.\n\n`
        : `2) Products\n` +
          `   • The theme ships empty. Add products in Shopify Admin → Products → Add product.\n\n`) +
      `Tip: Want us to do this automatically instead? Connect your Shopify store in\n` +
      `Yappaflow — we'll push the theme + products straight into your admin.\n\n` +
      `Generated by Yappaflow on ${new Date().toISOString()}.\n`,
    language: "text",
  });

  if (identity.products?.length) {
    allFiles.push({
      filePath: "products.csv",
      content:  buildProductsCsv(identity, identity.products),
      language: "csv",
    });
  }

  // Reclassify language for prefixed paths (defensive).
  for (const f of allFiles) f.language = languageForPath(f.filePath);

  for (const f of allFiles) {
    await GeneratedArtifact.create({
      agencyId,
      sessionId,
      projectId,
      filePath: f.filePath,
      content:  f.content,
      language: f.language,
      platform: "shopify",
      purpose:
        f.filePath === "products.csv" ? "shopify-products-csv" :
        f.filePath === "README.txt"   ? "shopify-readme" :
                                        "shopify-theme",
      version: 1,
    });
    await Project.findByIdAndUpdate(projectId, { $inc: { buildFilesDone: 1 } });
  }

  await Project.findByIdAndUpdate(projectId, {
    buildJobStatus:  "done",
    buildPhase:      "done",
    buildFilesTotal: allFiles.length,
    progress:        80,
  });
  await AISession.findByIdAndUpdate(sessionId, { phase: "ready", status: "completed" });

  log(`   → Generated ${allFiles.length} Shopify files for project ${projectId}`);
  return { filesCreated: allFiles.length };
}
