const TOKEN_COLOR_KEYS = [
  "color_bg", "color_fg", "color_primary", "color_on_primary",
  "color_surface", "color_muted", "color_border", "color_accent",
  "color_bg_dark", "color_fg_dark", "color_primary_dark", "color_on_primary_dark",
  "color_surface_dark", "color_muted_dark", "color_border_dark", "color_accent_dark",
] as const;

type TokenColorKey = typeof TOKEN_COLOR_KEYS[number];

interface BrandJson {
  tokens: Record<TokenColorKey, string> & {
    font_display_family: string;
    font_body_family:    string;
  };
  brand_name:    string;
  brand_tagline: string;
  brand_css:     string;
  hero: {
    eyebrow:    string;
    headline:   string;
    subhead:    string;
    cta_label:  string;
    cta_link:   string;
    cta2_label: string;
    cta2_link:  string;
  };
  sections: {
    feature_grid?: {
      heading: string; subheading?: string;
      blocks: Array<{ icon?: string; heading: string; body: string }>;
    };
    testimonials?: {
      heading: string; subheading?: string;
      blocks: Array<{ quote: string; author: string; role?: string }>;
    };
    faq?: {
      heading: string; subheading?: string;
      blocks: Array<{ question: string; answer: string }>;
    };
    cta?: {
      heading: string; subhead?: string;
      cta_label: string; cta_link: string;
    };
    featured_products?: {
      heading?: string; subheading?: string;
      cta_label?: string; cta_link?: string;
    };
  };
  content_for_index: string[];
}

const HEX_RE = /^#([0-9a-fA-F]{3}|[0-9a-fA-F]{6})$/;

function stripJsonFences(s: string): string {
  // Some models wrap JSON in markdown fences despite instructions.
  // Strip ```json / ``` at the boundaries, then trim.
  return s
    .trim()
    .replace(/^```(?:json)?\s*/i, "")
    .replace(/\s*```$/i, "")
    .trim();
}

function tryParseBrandJson(raw: string): { ok: true; value: BrandJson } | { ok: false; error: string } {
  try {
    const parsed = JSON.parse(stripJsonFences(raw));
    return { ok: true, value: parsed as BrandJson };
  } catch (err) {
    return { ok: false, error: (err as Error).message };
  }
}

function validateBrandJson(j: BrandJson | undefined | null): PatchShopifyBrandIssue[] {
  const issues: PatchShopifyBrandIssue[] = [];
  if (!j || typeof j !== "object") {
    issues.push({ path: "(root)", issue: "Output was not a JSON object" });
    return issues;
  }

  // tokens
  if (!j.tokens || typeof j.tokens !== "object") {
    issues.push({ path: "tokens", issue: "Missing tokens object" });
  } else {
    for (const k of TOKEN_COLOR_KEYS) {
      const v = j.tokens[k];
      if (typeof v !== "string" || !HEX_RE.test(v)) {
        issues.push({
          path:    `tokens.${k}`,
          issue:   `Missing or invalid hex color: ${v ?? "(missing)"}`,
          suggest: "Use #rrggbb format (e.g. #0a0a0a)",
        });
      }
    }
    for (const k of ["font_display_family", "font_body_family"] as const) {
      const v = j.tokens[k];
      if (typeof v !== "string" || v.trim().length < 3) {
        issues.push({ path: `tokens.${k}`, issue: "Missing font-family stack" });
      }
    }
  }

  // brand name + tagline
  if (typeof j.brand_name !== "string" || j.brand_name.trim() === "") {
    issues.push({ path: "brand_name", issue: "Missing brand_name" });
  }
  if (typeof j.brand_tagline !== "string") {
    issues.push({ path: "brand_tagline", issue: "Missing brand_tagline (can be empty string, not missing key)" });
  }

  // brand_css
  if (typeof j.brand_css !== "string") {
    issues.push({ path: "brand_css", issue: "Missing brand_css string" });
  }

  // hero
  if (!j.hero || typeof j.hero !== "object") {
    issues.push({ path: "hero", issue: "Missing hero object" });
  } else {
    for (const k of ["headline", "subhead", "cta_label", "cta_link"] as const) {
      if (typeof j.hero[k] !== "string" || j.hero[k].trim() === "") {
        issues.push({ path: `hero.${k}`, issue: `Missing hero.${k}` });
      }
    }
  }

  // sections (depth floors)
  const s = j.sections ?? {};
  if (!s.feature_grid || !Array.isArray(s.feature_grid.blocks) || s.feature_grid.blocks.length < 3) {
    issues.push({
      path:    "sections.feature_grid.blocks",
      issue:   "feature_grid needs at least 3 blocks (scaffold preset is 4)",
      suggest: "Return 4 feature blocks",
    });
  }
  if (!s.testimonials || !Array.isArray(s.testimonials.blocks) || s.testimonials.blocks.length < 2) {
    issues.push({ path: "sections.testimonials.blocks", issue: "testimonials needs at least 2 blocks" });
  }
  if (!s.faq || !Array.isArray(s.faq.blocks) || s.faq.blocks.length < 3) {
    issues.push({ path: "sections.faq.blocks", issue: "faq needs at least 3 blocks" });
  }
  if (!s.cta || typeof s.cta.heading !== "string") {
    issues.push({ path: "sections.cta.heading", issue: "Missing cta.heading" });
  }

  // content_for_index
  if (!Array.isArray(j.content_for_index) || j.content_for_index.length < 3) {
    issues.push({ path: "content_for_index", issue: "Need at least 3 section ids in content_for_index" });
  }

  return issues;
}

