/**
 * Deep-merge the brand-layer JSON into the scaffold's
 * `config/settings_data.json`, overwrite `assets/brand.css`, and inject
 * static_product fallback blocks into the featured-products section.
 *
 * Returns a NEW scaffold file array — input is not mutated.
 */
function applyBrandLayer(
  scaffold: ScaffoldFile[],
  brand: BrandJson,
  identity: IProjectIdentity,
): ScaffoldFile[] {
  const byPath = new Map(scaffold.map((f) => [f.filePath, { ...f }] as const));

  // settings_data.json
  const sdPath = "config/settings_data.json";
  const sdFile = byPath.get(sdPath);
  if (!sdFile) throw new Error(`Scaffold missing ${sdPath}`);

  const sd = JSON.parse(sdFile.content);
  const current = sd.current ?? {};

  // Tokens + brand identity → current.*
  Object.assign(current, brand.tokens);
  current.brand_name    = brand.brand_name;
  current.brand_tagline = brand.brand_tagline;

  // Section settings
  const sections = current.sections ?? {};

  // hero (schema matches sections/hero.liquid)
  sections.hero = {
    type: "hero",
    settings: {
      eyebrow:    brand.hero.eyebrow    ?? "",
      headline:   brand.hero.headline,
      subhead:    brand.hero.subhead,
      cta_label:  brand.hero.cta_label,
      cta_link:   brand.hero.cta_link,
      cta2_label: brand.hero.cta2_label ?? "",
      cta2_link:  brand.hero.cta2_link  ?? "",
      image:      "",
    },
  };

  // featured-products
  const fp = brand.sections.featured_products ?? {};
  const staticBlocks: Record<string, { type: string; settings: Record<string, unknown> }> = {};
  const staticBlockOrder: string[] = [];

  (identity.products ?? []).slice(0, 12).forEach((p, idx) => {
    const id = `sp_${idx}`;
    staticBlocks[id] = {
      type: "static_product",
      settings: {
        title:         p.name,
        description:   p.description ?? "",
        price:         `${p.currency ?? "$"}${p.price}`,
        compare_price: "",
        image:         "",
        image_url:     p.images?.[0] ?? "",
        link:          `/products/${toHandle(p.name)}`,
      },
    };
    staticBlockOrder.push(id);
  });

  sections["featured-products"] = {
    type: "featured-products",
    settings: {
      heading:            fp.heading    ?? "This week's picks",
      subheading:         fp.subheading ?? "",
      collection:         "all",
      products_per_row:   3,
      products_per_page:  6,
      show_vendor:        false,
      cta_label:          fp.cta_label  ?? "Browse all",
      cta_link:           fp.cta_link   ?? "/collections/all",
    },
    blocks:      staticBlocks,
    block_order: staticBlockOrder,
  };

  // feature-grid
  if (brand.sections.feature_grid) {
    const fg = brand.sections.feature_grid;
    const blocks: Record<string, { type: string; settings: Record<string, unknown> }> = {};
    const order: string[] = [];
    fg.blocks.slice(0, 8).forEach((b, idx) => {
      const id = `f_${idx}`;
      blocks[id] = {
        type: "feature",
        settings: { icon: b.icon ?? "star", heading: b.heading, body: b.body },
      };
      order.push(id);
    });
    sections["feature-grid"] = {
      type: "feature-grid",
      settings: { heading: fg.heading, subheading: fg.subheading ?? "" },
      blocks,
      block_order: order,
    };
  }

  // testimonials
  if (brand.sections.testimonials) {
    const t = brand.sections.testimonials;
    const blocks: Record<string, { type: string; settings: Record<string, unknown> }> = {};
    const order: string[] = [];
    t.blocks.slice(0, 9).forEach((b, idx) => {
      const id = `t_${idx}`;
      blocks[id] = {
        type: "testimonial",
        settings: { quote: b.quote, author: b.author, role: b.role ?? "" },
      };
      order.push(id);
    });
    sections["testimonials"] = {
      type: "testimonials",
      settings: { heading: t.heading, subheading: t.subheading ?? "" },
      blocks,
      block_order: order,
    };
  }

  // faq
  if (brand.sections.faq) {
    const q = brand.sections.faq;
    const blocks: Record<string, { type: string; settings: Record<string, unknown> }> = {};
    const order: string[] = [];
    q.blocks.slice(0, 20).forEach((b, idx) => {
      const id = `q_${idx}`;
      blocks[id] = {
        type: "item",
        settings: { question: b.question, answer: b.answer },
      };
      order.push(id);
    });
    sections["faq"] = {
      type: "faq",
      settings: { heading: q.heading, subheading: q.subheading ?? "" },
      blocks,
      block_order: order,
    };
  }

  // cta
  if (brand.sections.cta) {
    sections["cta"] = {
      type: "cta",
      settings: {
        heading:   brand.sections.cta.heading,
        subhead:   brand.sections.cta.subhead ?? "",
        cta_label: brand.sections.cta.cta_label,
        cta_link:  brand.sections.cta.cta_link,
      },
    };
  }

  current.sections          = sections;
  current.content_for_index = brand.content_for_index;
  sd.current                = current;

  byPath.set(sdPath, {
    filePath: sdPath,
    content:  JSON.stringify(sd, null, 2) + "\n",
    language: "json",
  });

  // brand.css overwrite
  const brandCssPath = "assets/brand.css";
  byPath.set(brandCssPath, {
    filePath: brandCssPath,
    content:  brand.brand_css.endsWith("\n") ? brand.brand_css : brand.brand_css + "\n",
    language: "css",
  });

  // Also drive templates/index.json's hero + featured-products settings
  // so the homepage sections reference brand copy even when a merchant
  // resets via theme editor. (Shopify merges `settings_data.json`
  // sections with the template's `sections` by id — we update both.)
  const idxPath = "templates/index.json";
  const idxFile = byPath.get(idxPath);
  if (idxFile) {
    const idx = JSON.parse(idxFile.content);
    if (idx.sections?.hero)              idx.sections.hero.settings              = sections.hero.settings;
    if (idx.sections?.["featured-products"]) {
      idx.sections["featured-products"].settings   = sections["featured-products"].settings;
      idx.sections["featured-products"].blocks     = sections["featured-products"].blocks;
      idx.sections["featured-products"].block_order = sections["featured-products"].block_order;
    }
    if (idx.sections?.["feature-grid"] && sections["feature-grid"]) {
      idx.sections["feature-grid"] = sections["feature-grid"];
    }
    if (idx.sections?.testimonials     && sections.testimonials)     idx.sections.testimonials = sections.testimonials;
    if (idx.sections?.faq              && sections.faq)              idx.sections.faq          = sections.faq;
    if (idx.sections?.cta              && sections.cta)              idx.sections.cta          = sections.cta;
    idx.order = brand.content_for_index;

    byPath.set(idxPath, {
      filePath: idxPath,
      content:  JSON.stringify(idx, null, 2) + "\n",
      language: "json",
    });
  }

  return Array.from(byPath.values());
}

