/**
 * Shopify "one-click import" bundle generator — scaffold-first edition.
 *
 * Yappaflow used to ask the AI to produce a full Shopify 2.0 theme from
 * scratch on every build: ~17 Liquid/JSON/CSS/JS files in a single
 * 32k-token generation, wrapped in a self-critique retry loop against a
 * Liquid validator. That worked but was expensive (tokens + wall-clock)
 * and fragile — a single dropped closing fence or missing `snippets/`
 * file would sink the whole bundle.
 *
 * The pipeline is now split in two cleanly-separated layers:
 *
 *   1. **Scaffold layer (static).** A complete, audited Shopify theme
 *      lives at `server/scaffolds/shopify-base/`: header, footer, cart,
 *      product page, collection, search, customer accounts, 404, the
 *      lot. The CSS is brand-agnostic and reads from CSS custom
 *      properties. Every file in the scaffold is copied verbatim into
 *      the generated bundle.
 *
 *   2. **Brand layer (AI).** A single narrow prompt emits one JSON
 *      object with design tokens, a small `brand.css`, and homepage
 *      section settings. That JSON is merged into the scaffold's
 *      `config/settings_data.json` plus written out as
 *      `assets/brand.css`. No Liquid is ever generated by the AI.
 *
 * Additional deterministic steps done server-side, NOT by the AI:
 *
 *   • Static product fallback. `sections/featured-products.liquid`
 *     ships with a hybrid render path: loop `collection.products` if
 *     present, else render static blocks. The blocks are written into
 *     `settings_data.json` here, one per entry in
 *     `Project.identity.products`. That way the ZIP preview (before
 *     the merchant imports products.csv) already shows the right
 *     product cards — fixing the "products not visible in generated
 *     theme" bug.
 *
 *   • `products.csv`. Built deterministically from
 *     `Project.identity.products`, not touched by the AI.
 *
 *   • `README.txt`. Top-level one-click upload instructions.
 *
 * The legacy Liquid validator / snippet-fallback / patch-regen loop is
 * retired — the scaffold is the "validator" now. All that remains to
 * validate at build time is the brand JSON itself (all required keys
 * present, colors parse, fonts are stacks).
 */

